    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (linha != -1) {
            try (Connection conn = Conexao.getConnection()) {
                Statement stmt = conn.createStatement();
                String sql = "delete from projeto where idprojeto ='" + (jTable1.getValueAt(linha, 0).toString() + "'");
                stmt.execute(sql);
                model.removeRow(linha);
                conn.close();
                stmt.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro a tentar excluir o registro");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Você não selecionou uma linha");
        }
        this.popularJTable("SELECT * FROM projeto ORDER BY idprojeto DESC");
    }//GEN-LAST:event_jButton3ActionPerformed

