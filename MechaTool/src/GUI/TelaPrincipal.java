    public void popularJTable(String sql) {

        try {
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/projeto", "root", "");
            PreparedStatement banco = (PreparedStatement) conn.prepareStatement(sql);
            banco.execute(); // cria o vetor

            ResultSet resultado = banco.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            
            
            while (resultado.next()) {
                model.addRow(new Object[]{
                    //retorna os dados da tabela do BD, cada campo e um coluna.
                    resultado.getString("idprojeto"),
                    resultado.getString("nomeEmp"),
                    resultado.getString("nomeResp"),
                    resultado.getString("nomeProjeto"),
                    resultado.getString("contato"),});
            }
            banco.close();
            conn.close();
        } catch (SQLException ex) {
            System.out.println("o erro foi " + ex);
        }

    }

